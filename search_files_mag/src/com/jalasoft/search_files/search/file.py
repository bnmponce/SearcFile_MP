class File:

    def set_path(self, path):
        self.path = path

    def get_path(self):
        return self.path

    def set_size(self, size):
        self.size = size

    def get_size(self):
        return self.size

    def set_is_file(self, flag):
        self.flag = flag

    def get_is_file(self):
        return self.flag

    def set_extension(self, extension):
        self.extension = extension

    def get_extension(self):
        return self.extension